<div :style='{"width":"1200px","padding":"20px","boxShadow":"0px 4px 10px 0px rgba(0,0,0,0.302)","margin":"10px auto","position":"relative","background":"#fff"}'>
    <el-form
      class="add-update-preview"
      ref="ruleForm"
      :model="ruleForm"
      :rules="rules"
      label-width="110px"
    >
          <el-form-item :style='{"width":"80%","padding":"10px 9%","margin":"0 0 10px","background":"#fff"}' label="作业名称" prop="zuoyemingcheng">
            <el-input v-model="ruleForm.zuoyemingcheng" 
                placeholder="作业名称" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"80%","padding":"10px 9%","margin":"0 0 10px","background":"#fff"}' label="班级" prop="banji">
            <el-input v-model="ruleForm.banji" 
                placeholder="班级" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"80%","padding":"10px 9%","margin":"0 0 10px","background":"#fff"}' label="封面" v-if="type!='cross' || (type=='cross' && !ro.fengmian)" prop="fengmian">
            <file-upload
            tip="点击上传封面"
            action="file/upload"
            :limit="3"
            :multiple="true"
            :fileUrls="ruleForm.fengmian?ruleForm.fengmian:''"
            @change="fengmianUploadChange"
            ></file-upload>
          </el-form-item>
            <el-form-item :style='{"width":"80%","padding":"10px 9%","margin":"0 0 10px","background":"#fff"}' class="upload" v-else label="封面" prop="fengmian">
                <img v-if="ruleForm.fengmian.substring(0,4)=='http'" class="upload-img" style="margin-right:20px;" v-bind:key="index" :src="ruleForm.fengmian.split(',')[0]" width="100" height="100">
                <img v-else class="upload-img" style="margin-right:20px;" v-bind:key="index" v-for="(item,index) in ruleForm.fengmian.split(',')" :src="baseUrl+item" width="100" height="100">
            </el-form-item>
          <el-form-item :style='{"width":"80%","padding":"10px 9%","margin":"0 0 10px","background":"#fff"}' label="布置时间" prop="buzhishijian" >
              <el-date-picker
                  value-format="yyyy-MM-dd HH:mm:ss"
                  v-model="ruleForm.buzhishijian" 
                  type="datetime"
                  placeholder="布置时间">
              </el-date-picker>
          </el-form-item>
          <el-form-item :style='{"width":"80%","padding":"10px 9%","margin":"0 0 10px","background":"#fff"}' label="截止时间" prop="jiezhishijian" >
              <el-date-picker
                  value-format="yyyy-MM-dd HH:mm:ss"
                  v-model="ruleForm.jiezhishijian" 
                  type="datetime"
                  placeholder="截止时间">
              </el-date-picker>
          </el-form-item>
          <el-form-item :style='{"width":"80%","padding":"10px 9%","margin":"0 0 10px","background":"#fff"}' label="教师账号" prop="jiaoshizhanghao">
            <el-input v-model="ruleForm.jiaoshizhanghao" 
                placeholder="教师账号" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"80%","padding":"10px 9%","margin":"0 0 10px","background":"#fff"}' label="教师姓名" prop="jiaoshixingming">
            <el-input v-model="ruleForm.jiaoshixingming" 
                placeholder="教师姓名" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"80%","padding":"10px 9%","margin":"0 0 10px","background":"#fff"}' label="作业内容" prop="zuoyeneirong">
            <editor 
                :style='{"padding":"0","boxShadow":"0px 4px 10px 0px rgba(0,0,0,0.302)","margin":"0","borderColor":"#ccc","backgroundColor":"#fff","borderRadius":"0","borderWidth":"0","width":"100%","borderStyle":"solid","height":"auto"}'
                v-model="ruleForm.zuoyeneirong" 
                class="editor" 
                action="file/upload">
            </editor>
          </el-form-item>

      <el-form-item :style='{"padding":"0","margin":"0 40px"}'>
        <el-button :style='{"border":"0","cursor":"pointer","padding":"0","margin":"0 20px 0 0","outline":"none","color":"rgba(255, 255, 255, 1)","borderRadius":"4px","background":"#5c39bb","width":"128px","lineHeight":"40px","fontSize":"14px","height":"40px"}'  type="primary" @click="onSubmit">提交</el-button>
        <el-button :style='{"border":"0px solid rgba(64, 158, 255, 1)","cursor":"pointer","padding":"0","margin":"0","outline":"none","color":"#fff","borderRadius":"4px","background":"#f3a9a0","width":"128px","lineHeight":"40px","fontSize":"14px","height":"40px"}' @click="back()">返回</el-button>
